    private double calculateTotal() throws java.text.ParseException {
        double total = 0.00;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (int row = 0; row < model.getRowCount(); row++) {
            Object value = model.getValueAt(row, 1); // Get the value from the second column

            // Parse the value as a double and add it to the total
            try {
                double amount = NumberFormat.getNumberInstance().parse(value.toString()).doubleValue();
                total += amount;
            } catch (ParseException e) {
                e.printStackTrace();
            }
        }

        return total;
    }

