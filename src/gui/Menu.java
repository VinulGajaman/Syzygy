    public static String generateOrderNumber() {
        // Use the current timestamp
        long timestamp = System.currentTimeMillis();

        // Format the timestamp to include date and time information
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyyMMddHHmmss");
        String formattedTimestamp = dateFormat.format(new Date(timestamp));

        // Combine formatted timestamp with additional information if needed
        String orderNumber = "ORD" + formattedTimestamp;

        return orderNumber;
    }

