    public void loadOrders() {

        try {

            ResultSet rs = MySQL.search("SELECT * FROM `order`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("order_number"));
                v.add(rs.getString("total"));
                 v.add(rs.getString("status"));
                dtm.addRow(v);
            }

        } catch (Exception e) {

            e.printStackTrace();

        }

    }
